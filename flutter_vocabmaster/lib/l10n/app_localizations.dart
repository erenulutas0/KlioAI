class AppLocalizations {
  AppLocalizations(this.locale);

  final Locale locale;

  static const Locale fallbackLocale = Locale('en');
  static const List<Locale> supportedLocales = <Locale>[
    Locale('en'),
    Locale('de'),
    Locale('tr'),
    Locale('ar'),
    Locale('zh'),
  ];

  static const LocalizationsDelegate<AppLocalizations> delegate =
      _AppLocalizationsDelegate();

  static AppLocalizations of(BuildContext context) {
    final localizations =
        Localizations.of<AppLocalizations>(context, AppLocalizations);
    return localizations ?? AppLocalizations(fallbackLocale);
  }

  static Locale normalize(Locale locale) {
    for (final supported in supportedLocales) {
      if (supported.languageCode == locale.languageCode) {
        return supported;
      }
    }
    return fallbackLocale;
  }

  String t(String key) {
    final code = _translations.containsKey(locale.languageCode)
        ? locale.languageCode
        : fallbackLocale.languageCode;
    return _translations[code]?[key] ??
        _translations[fallbackLocale.languageCode]?[key] ??
        key;
  }

  static String languageName(String code) {
    switch (code) {
      case 'tr':
        return 'Turkce';
      case 'de':
        return 'Deutsch';
      case 'ar':
        return 'العربية';
      case 'zh':
        return '中文';
      default:
        return 'English';
    }
  }

